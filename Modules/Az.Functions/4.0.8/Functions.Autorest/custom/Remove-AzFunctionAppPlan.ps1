        [ValidateNotNullOrEmpty()]
        ${Name},

        [Parameter(ParameterSetName='ByName', Mandatory=$true)]
        [Microsoft.Azure.PowerShell.Cmdlets.Functions.Category('Path')]
        [System.String]
        [ValidateNotNullOrEmpty()]
